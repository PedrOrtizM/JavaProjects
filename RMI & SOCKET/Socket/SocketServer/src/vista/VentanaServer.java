    private void actualizar() {
        Iterator it = server.getListaConectados().entrySet().iterator();
        modelo.setNumRows(0);
        while (it.hasNext()) {
            Map.Entry e = (Map.Entry) it.next();
            modelo.addRow(new Object[]{e.getKey()});
        }

    }


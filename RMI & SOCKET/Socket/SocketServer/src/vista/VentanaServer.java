    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Iterator it = server.getListaConectados().entrySet().iterator();

        while (it.hasNext()) {
            Map.Entry e = (Map.Entry) it.next();
            if (e.getKey().equals(JTconectados.getValueAt(JTconectados.getSelectedRow(), 0))) {
                AtiendeCliente atiende = (AtiendeCliente) e.getValue();
                for (Computador l : atiende.getIn().getListaComputadores()) {
                    if (JTconectados.getValueAt(JTconectados.getSelectedRow(), 0).equals(e.getKey())) {
                        JOptionPane.showMessageDialog(null, ""
                                + "Nombre Usuario:         " + l.getUsuario()
                                + "\nSistema Operativo:    " + l.getSo()
                                + "\nDescripcion del SO:   " + l.getDescripcionSO()
                                + "\nArquitectura de SO:   " + l.getArquitecturaSO()
                                + "\nMemoria RAM:          " + l.getRam()
                                + "\nProcesador:           " + l.getProcesador()
                                + "\nFabricante:           " + l.getFabricanteProcesador()
                                + "\nNumero de nucleos:    " + l.getNucleoProcesador()
                                + "\nDireccion IP:         " + l.getIp()
                                + "\nMAC:                  " + l.getMac()
                        );
                    }
                }
            }

        }


    }//GEN-LAST:event_jButton2ActionPerformed




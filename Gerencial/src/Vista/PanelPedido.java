    private void jButton20ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton20ActionPerformed

        ArrayList<String> referencia = new ArrayList<>();
        ArrayList<Integer> cantidad = new ArrayList<>();

        DAOproveedor dao = new DAOproveedor();
        DAOpedido dao2 = new DAOpedido();

        for (int i = 0; i < JTmodel.getRowCount(); i++) {

            referencia.add(JTmodel.getValueAt(i, 2).toString() + "");
            cantidad.add(Integer.parseInt(JTmodel.getValueAt(i, 1).toString()));

        }

        Date fecha = new Date();
        Pedido pedido = new Pedido();
        pedido.setFecha(fecha);

        pedido.setProveedor(dao.verificarProveedor(JTrazonSocial.getText()));

        dao2.setPedido(pedido);
        dao2.registrarPedido();

        if (dao2.obtenerIdPedido() != -1) {
            pedido.setIdPedido(dao2.obtenerIdPedido());

            pedido.setCantidad(cantidad);
            pedido.setReferencia(referencia);
            dao2.setPedido(pedido);

            dao2.registrarHas();
        } else {
            JOptionPane.showMessageDialog(null, "ERROR EN LA CONSULTA");
        }


    }//GEN-LAST:event_jButton20ActionPerformed


    private void jButton22ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton22ActionPerformed
        DAOproducto dao = new DAOproducto();

        System.out.println(dao.verificarProducto(JTreferencia.getText()));

        if (dao.verificarProducto(JTreferencia.getText()) != null) {
            String producto = dao.verificarProducto(JTreferencia.getText()).getNombre();
            int cantidad;

            cantidad = (int) JTcantidad.getValue();
            JTmodel.addRow(new Object[]{producto, cantidad, JTreferencia.getText()});

        } else {

            int op = JOptionPane.showConfirmDialog(null, "Producto no registrado. Registrarlo ahora");

            if (op == 0) {
                PanelProducto panel = new PanelProducto(null, true);
                panel.setVisible(true);
                panel.setLocationRelativeTo(null);
            }
        }
    }//GEN-LAST:event_jButton22ActionPerformed

